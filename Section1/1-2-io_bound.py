"""  # =================================================================================================================
Section 1 - 2: IO Bound
------------------------------------------------------------------------------------------------------------------------
[ IO Bound ] ----------------------------------------------------------------------------------------------------------
- I : Input, O : Output
- 프로그램이 실행될 때 실행 속도가 I/O에 의해 제한됨을 의미한다.
    (1) 사용자가 입력을 하고 해당하는 입력에 대해 더하기 100을 한 결과값을 출력해주는 프로그램을 가정할 때, input이 들어올 때까지 대기
    (2) 사용자가 키보드로 숫자를 입력하는 경우 뿐만 아니라, 컴퓨터와 컴퓨터끼리 통신을 할 떄에도 I/O 바운드가 발생 (Network I/O)
------------------------------------------------------------------------------------------------------------------------
[ Blocking ] -----------------------------------------------------------------------------------------------------------
- 바운드에 의해 코드가 멈추게 되는 현상
- Non-blocking : 바운드에 의해 코드가 멈추지 않는 것
------------------------------------------------------------------------------------------------------------------------
"""  # =================================================================================================================
