# import ===============================================================================================================
import time
# ======================================================================================================================


def delivery(name, mealtime):
    print(f"Successfully delivered to {name}")
    time.sleep(mealtime)
    print(f"{name} have eaten, spending {mealtime} sec.")
    print(f"Complete to pick up plates from {name}")


def main():
    delivery("A", 5)
    delivery("B", 3)
    delivery("C", 4)


if __name__ == "__main__":
    start = time.time()
    main()
    end = time.time()
    print(end - start)
